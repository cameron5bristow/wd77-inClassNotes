/* A literal is a Literal representation of a value on JS. It is a fixed value - not a variable - they are literally provided in the code

A variable is not a literal, the information assigned to the variable is the literal
        ex:
        (keyword)                    (value/literal)
            let        placeholder = 'text';
                        (variable)
*/

